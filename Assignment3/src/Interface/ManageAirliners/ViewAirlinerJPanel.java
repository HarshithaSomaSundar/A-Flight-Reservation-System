    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int n = JOptionPane.showConfirmDialog(
            null,
            "Do you really want to delete?",
            "Delete Confirmation",
            JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION)
        {
            int selectedrow= flightSchTable.getSelectedRow();
            if(selectedrow<0)
            {
                JOptionPane.showMessageDialog(null, "Please select a flight to remove ", "Warning", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                Flights i =(Flights) flightSchTable.getValueAt(selectedrow, 0);
                s.getFlightDir().removeFlight(i);
                refreshTable();

            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

