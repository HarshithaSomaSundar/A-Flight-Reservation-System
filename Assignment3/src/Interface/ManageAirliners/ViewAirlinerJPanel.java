     public void refreshTable() {
        int rowCount = flightSchTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)flightSchTable.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Flights f : s.getFlightDir().getFlightsDirtList()) {
            Object row[] = new Object[3];
            row[0] = f;
            row[1]=f.getSource();
            row[2]=f.getDestination();
            model.addRow(row);
        }
     }

